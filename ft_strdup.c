#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t		i;
	char		*str;

	str = ft_strnew(ft_strlen(s1));
	if (!str)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		++i;
	}
	return (str);
}
