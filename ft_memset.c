#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	size_t	i;

	if (!s || !len)
		return (NULL);
	i = 0;
	while (i < len)
	{
		((unsigned char *)s)[i] = c;
		++i;
	}
	return (s);
}
