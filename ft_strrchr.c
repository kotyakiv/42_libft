#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;
	size_t	last;

	i = 0;
	last = 0;
	while (s[i])
	{
		if (s[i] == c)
			last = i;
		++i;
	}
	return ((char *)s + last);
}
